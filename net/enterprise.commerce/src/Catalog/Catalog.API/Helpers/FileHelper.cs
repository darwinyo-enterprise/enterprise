namespace Catalog.API.Helpers
{
    /// <summary>
    /// File Related work helper
    /// </summary>
    public class FileHelper
    {
        /// <summary>
        /// Get Image Mime By extension
        /// </summary>
        /// <param name="extension">
        /// extension name
        /// example : .png, .gif, .jpg/.jpeg, so on.
        /// </param>
        /// <returns></returns>
        public static string GetImageMimeTypeFromImageFileExtension(string extension)
        {
            string mimetype;

            switch (extension)
            {
                case ".png":
                    mimetype = "image/png";
                    break;
                case ".gif":
                    mimetype = "image/gif";
                    break;
                case ".jpg":
                case ".jpeg":
                    mimetype = "image/jpeg";
                    break;
                case ".bmp":
                    mimetype = "image/bmp";
                    break;
                case ".tiff":
                    mimetype = "image/tiff";
                    break;
                case ".wmf":
                    mimetype = "image/wmf";
                    break;
                case ".jp2":
                    mimetype = "image/jp2";
                    break;
                case ".svg":
                    mimetype = "image/svg+xml";
                    break;
                default:
                    mimetype = "application/octet-stream";
                    break;
            }

            return mimetype;
        }
    }
}
